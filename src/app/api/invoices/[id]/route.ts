/**
 * Invoice Download API
 * Generates and downloads professional PDF invoice for The Yard Restaurant
 */

import { NextRequest, NextResponse } from 'next/server';
import { getAdminDb } from '@/lib/firebase-admin';
import { format } from 'date-fns';

interface InvoiceItem {
  name: string;
  quantity: number;
  price: number;
  subtotal: number;
}

interface Invoice {
  id: string;
  invoiceNumber: string;
  orderId: string;
  userId: string;
  customerName: string;
  customerEmail?: string;
  customerPhone?: string;
  items: InvoiceItem[];
  subtotal: number;
  serviceCharge: number;
  tax: number;
  total: number;
  paymentMethod: string;
  paymentStatus: string;
  paidAt?: Date;
  createdAt: Date;
  restaurant?: {
    name: string;
    address: string;
    phone: string;
    email: string;
  };
}

/**
 * Generate professional invoice text (PDF would use a library like jspdf or pdfkit)
 */
function generateInvoiceText(invoice: Invoice): string {
  const divider = '='.repeat(50);
  const line = '-'.repeat(50);
  
  const text = `
${divider}
                    THE YARD RESTAURANT
                      🌿 Fine Dining 🌿
${divider}

                    INVOICE

Invoice Number: ${invoice.invoiceNumber}
Date: ${invoice.createdAt instanceof Date ? format(invoice.createdAt, 'MMMM dd, yyyy') : format(new Date(invoice.createdAt), 'MMMM dd, yyyy')}
${invoice.paidAt ? `Paid: ${invoice.paidAt instanceof Date ? format(invoice.paidAt, 'MMMM dd, yyyy HH:mm') : format(new Date(invoice.paidAt), 'MMMM dd, yyyy HH:mm')}` : ''}

${line}
RESTAURANT DETAILS:
${line}
The Yard Restaurant
737 Rue Batibois
Douala, Cameroon
Phone: +237 671 490 733
Email: info@theyardrestaurant.com

${line}
BILL TO:
${line}
Customer: ${invoice.customerName}
${invoice.customerEmail ? `Email: ${invoice.customerEmail}` : ''}
${invoice.customerPhone ? `Phone: ${invoice.customerPhone}` : ''}

${line}
ORDER DETAILS:
${line}
${invoice.items.map(item => 
  `${item.name}
   ${item.quantity} x ${item.price.toLocaleString()} XAF = ${item.subtotal.toLocaleString()} XAF`
).join('\n')}

${line}
PAYMENT SUMMARY:
${line}
Subtotal:                           ${invoice.subtotal.toLocaleString()} XAF
Service Charge (5%):                ${invoice.serviceCharge.toLocaleString()} XAF
VAT (19.25%):                       ${invoice.tax.toLocaleString()} XAF
${line}
GRAND TOTAL:                        ${invoice.total.toLocaleString()} XAF
${divider}

Payment Method: ${invoice.paymentMethod}
Payment Status: ${invoice.paymentStatus}

${divider}
        Thank you for dining with us!
         We hope to see you again.

      Follow us: @theyardrestaurant
${divider}

Order Reference: #${invoice.orderId?.slice(-6).toUpperCase() || 'N/A'}

IMPORTANT NOTICE:
- This is a computer-generated invoice.
- Payment is non-refundable after 24 hours.
- For queries, contact: +237 671 490 733

Generated by The Yard Restaurant POS System
`.trim();

  return text;
}

