/**
 * GET /api/invoices/[id]/download
 * Download invoice as HTML or text file
 */
export async function GET(
  request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  try {
    const { id } = await params;
    const adminDb = getAdminDb();
    
    if (!adminDb) {
      return NextResponse.json(
        { error: 'Database unavailable' },
        { status: 503 }
      );
    }
    
    // Get invoice
    const invoiceDoc = await adminDb.collection('invoices').doc(id).get();
    
    if (!invoiceDoc.exists) {
      // Try to find by orderId
      const invoiceQuery = await adminDb.collection('invoices')
        .where('orderId', '==', id)
        .limit(1)
        .get();
      
      if (invoiceQuery.empty) {
        return NextResponse.json(
          { error: 'Invoice not found' },
          { status: 404 }
        );
      }
      
      const foundDoc = invoiceQuery.docs[0];
      const invoice = foundDoc.data() as Invoice;
      invoice.id = foundDoc.id;
      
      const { searchParams } = new URL(request.url);
      const format = searchParams.get('format') || 'html';
      
      if (format === 'text') {
        const text = generateInvoiceText(invoice);
        return new NextResponse(text, {
          headers: {
            'Content-Type': 'text/plain',
            'Content-Disposition': `attachment; filename="invoice-${invoice.invoiceNumber}.txt"`,
          },
        });
      }
      
      const html = generateInvoiceHTML(invoice);
      return new NextResponse(html, {
        headers: {
          'Content-Type': 'text/html',
          'Content-Disposition': `attachment; filename="invoice-${invoice.invoiceNumber}.html"`,
        },
      });
    }
    
    const invoice = { id: invoiceDoc.id, ...invoiceDoc.data() } as Invoice;
    
    const { searchParams } = new URL(request.url);
    const format = searchParams.get('format') || 'html';
    
    if (format === 'text') {
      const text = generateInvoiceText(invoice);
      return new NextResponse(text, {
        headers: {
          'Content-Type': 'text/plain',
          'Content-Disposition': `attachment; filename="invoice-${invoice.invoiceNumber}.txt"`,
        },
      });
    }
    
    const html = generateInvoiceHTML(invoice);
    return new NextResponse(html, {
      headers: {
        'Content-Type': 'text/html',
        'Content-Disposition': `attachment; filename="invoice-${invoice.invoiceNumber}.html"`,
      },
    });
    
  } catch (error) {
    console.error('Error downloading invoice:', error);
    return NextResponse.json(
      { error: 'Failed to download invoice' },
      { status: 500 }
    );
  }
}
